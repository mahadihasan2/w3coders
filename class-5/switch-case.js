
// switch-Case Practice 

var color = "red";
switch (color) {
    case "green":
        console.log("My Favorite Color is Green");
        break
    case "red":
        console.log("My Favorite color is RED");
        break
    case "white":
        console.log("My favorite color is White");
        break
    default:
        console.log("My Favorite is not Matching")

}